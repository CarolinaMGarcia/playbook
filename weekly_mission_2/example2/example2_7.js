// Ejemplo 8: Uso de filter para filtrar una lista de elementos
const fruts = ['mango', 'manzana', 'pera', 'uva', 'fresa']
const frutsContainana = fruts.filter((fruts) => // esta es una función
  fruts.includes('ana') // indicación para solo filtrar elementos que incluyan "ana"
)
console.log("Ejemplo 7: Uso de filter para filtrar una lista de elementos")
console.log(frutsContainana)
